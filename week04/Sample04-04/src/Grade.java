public class Grade {
    private char grade;
    private int baseSalary;

    public Grade(char grade, int baseSalary) {
        this.grade = grade;
        this.baseSalary = baseSalary;
    }

    public char getGrade() {
        return grade;
    }

    public int getBaseSalary() {
        return baseSalary;
    }
}